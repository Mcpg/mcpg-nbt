package pl.mcpg.nbt;

public class Tag<T>
{
    protected final TagType type;
    protected String name;
    protected T value;

    public Tag(String name, T value, TagType type)
    {
        this.name = name;
        this.value = value;
        this.type = type;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public T getValue()
    {
        return value;
    }

    public void setValue(T value)
    {
        this.value = value;
    }

    public TagType getType()
    {
        return type;
    }

    @Override
    public boolean equals(Object obj)
    {
        if (!(obj instanceof Tag<?>))
        {
            return false;
        }
        return ((Tag<?>) obj).getValue().equals(value);
    }
}
